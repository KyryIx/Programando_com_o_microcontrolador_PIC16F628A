; exemplo de delay com três níveis
  MOVLW   .10   ; (1)
  MOVWF   0x20  ; (2)
  MOVLW   .200  ; (3)
  MOVWF   0x21  ; (4)
  MOVLW   .133  ; (5)
  MOVWF   0x22  ; (6)
loop :          ; (7)  <------------+ <--+ <--+
                ; (8)               |    |    |
  DECFSZ  0x20  ; (9)  >--+----+    |    |    |
                ; (10)    |    |    |    |    |
  GOTO    loop  ; (11) <--+    | >--+    |    |
                ; (12)         |         |    |
  DECFSZ  0x21  ; (13) >--+ <--+ >--+    |    |
                ; (14)    |         |    |    |
  GOTO    loop  ; (15) <--+         | >--+    |
                ; (16)              |         |
  DECFSZ  0x22  ; (17) >--+ <-------+ >--+    |
                ; (18)    |              |    |
  GOTO    loop  ; (19) <--+              | >--+
                ; (20)                   |
                ; (21) <-----------------+
